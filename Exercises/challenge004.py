algo=input('Digite algo:')
print('O tipo primitivo desse valor é:', type(algo))
print('É um número?', "\033[33m", algo.isnumeric(), "\033[m")
print('É alfabético?', "\033[33m", algo.isalpha(), "\033[m")
print('É alfanumérico?', "\033[33m", algo.isalnum(), "\033[m")
print('Está em maiúsculo?', "\033[33m", algo.isupper(), "\033[m")
print('Tem apenas espaços?', "\033[33m", algo.isspace(), "\033[m")
print('Está em minúsculo?', "\033[33m", algo.islower(), "\033[m")
print('Está capitalizado?', "\033[33m", algo.istitle(), "\033[m")
